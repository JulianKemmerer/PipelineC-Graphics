#ifndef __PIPELINEC_FLOAT_TYPE_H__
#define __PIPELINEC_FLOAT_TYPE_H__

#ifndef float_mul
float float_mul(float a, float b) { return a*b; }
#endif
#ifndef float_add
float float_add(float a, float b) { return a+b; }
#endif
#ifndef float_sub
float float_sub(float a, float b) { return a-b; }
#endif
#ifndef float_div
float float_div(float a, float b) { return a/b; }
#endif

#endif //__PIPELINEC_FLOAT_TYPE_H__
